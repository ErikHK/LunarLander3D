public class Vec3 {
    public float x,y,z;

    public Vec3(float x_, float y_, float z_)
    {
        this.x = x_;
        this.y = y_;
        this.z = z_;

    }

    public Vec3()
    {
        this.x = 0;
        this.y = 0;
        this.z = 0;

    }
}
